# -*- coding: utf-8 -*-
"""
Простой веб‑сервис на Flask, который принимает аудиофайл,
применяет к нему цепочку обработки (прослушивание → осмысление → озвучивание)
и возвращает готовый WAV‑файл в ответе.

Для работы требуется:
    * Flask
    * модули core.listen, core.speak, core.think
    * утилита form_input_filename из utils.utils
"""

# -------------------------------------------------------------------------
# 1. Импорт необходимых зависимостей
# -------------------------------------------------------------------------
from flask import Flask, send_file, request          # базовый веб‑фреймворк
from flask.cli import load_dotenv                    # подгрузка переменных окружения из .env

# Загрузка переменных окружения (например, конфиги доступа к API)
load_dotenv()

# Модули бизнес‑логики проекта (они находятся в пакете `core`)
from core.listen import listen   # читаем аудио и получаем «сырой» ввод
from core.think import think     # обрабатываем полученный ввод (например, распознаём речь, генерируем ответ)
from core.speak import speak     # превращаем текстовый ответ в аудио (wav)

# Утилита, формирующая безопасный путь для сохранения загруженного файла
from utils.utils import form_input_filename


# -------------------------------------------------------------------------
# 2. Создаём экземпляр приложения Flask
# -------------------------------------------------------------------------
app = Flask(__name__)


# -------------------------------------------------------------------------
# 3. Описание эндпоинта /ask/
#    Принимает POST‑запрос с полем "file" (multipart/form-data)
# -------------------------------------------------------------------------
@app.route("/ask/", methods=['POST'])
def ask():
    """
    Обрабатывает загруженный пользователем аудиофайл:
        1) проверяет наличие файла в запросе;
        2) сохраняет его во временное место;
        3) последовательно вызывает listen → think → speak;
        4) отправляет полученный wav‑файл клиенту.
    """
    # -------------------------------------------------
    # Проверка наличия части "file" в запросе
    # -------------------------------------------------
    if 'file' not in request.files:
        return {'error': 'No file part'}, 400

    # Получаем объект FileStorage
    file = request.files['file']

    # -------------------------------------------------
    # Проверка, что пользователь действительно выбрал файл
    # -------------------------------------------------
    if file.filename == '':
        return {'error': 'No selected file'}, 400

    # -------------------------------------------------
    # Формируем безопасный путь для сохранения входного файла
    # (например, убираем опасные символы, добавляем директорию temp/)
    # -------------------------------------------------
    input_filename = form_input_filename(file.filename)

    # Сохраняем загруженный файл на диск
    file.save(input_filename)

    # -------------------------------------------------
    # Основная цепочка обработки:
    #   listen  →  think  →  speak
    #   speak возвращает путь к готовому wav‑файлу
    # -------------------------------------------------
    processed_wav_path = speak(think(listen(input_filename)))

    # -------------------------------------------------
    # Отправляем клиенту готовый wav‑файл как вложение.
    #   mimetype   – тип содержимого,
    #   as_attachment=True – заставляем браузер предложить «Сохранить как»,
    #   download_name – имя файла в скачивании.
    # -------------------------------------------------
    return send_file(
        processed_wav_path,
        mimetype="audio/wav",
        as_attachment=True,
        download_name="processed_audio.wav"
    )


# -------------------------------------------------------------------------
# 4. Точка входа программы
# -------------------------------------------------------------------------
if __name__ == '__main__':
    # В режиме отладки Flask автоматически перезапускается при изменениях кода.
    app.run(debug=True)